// Table lookup method is derived from tkng's micter source code.
double utf8len2(const string &s)
{
    double len = 0;
    size_t i = 0;
    size_t s_size = s.size();
    unsigned char c;
    while(i < s_size){
        c = s[i];
        int l = utf8length[c];
        i += l;
        if ( l == 1 ) {
            if ( c == '.' || c == ',' )
                len += 0.2;
            else if (c == 'i')
                len += 0.5;
            else if ( c == ' ' )
                len += 0.7;
            else {
                if ('a' <= c && c <= 'z') len += 0.8;
                else len += 0.9;
            }
        }
        else
            len++;
    }
    return len;
}
